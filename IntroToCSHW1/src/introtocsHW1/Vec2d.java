package introtocsHW1;

/**
 * A 2d vector class.
 * @author Dov Neimand
 */
public class Vec2d {
    public final double x, y;

    /**
     * The constructor.
     * @param x The x value.
     * @param y The y value.
     */
    public Vec2d(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
